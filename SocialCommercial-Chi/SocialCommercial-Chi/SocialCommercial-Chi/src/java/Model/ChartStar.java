package Model;

import java.sql.Date;

public class ChartStar {
    private Date date;
    private double value;

    public ChartStar() {
    }

    public ChartStar(Date date, double value) {
        this.date = date;
        this.value = value;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public double getValue() {
        return value;
    }

    public void setValue(double value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "ChartStar{" + "date=" + date + ", value=" + value + '}';
    }
    
    
}
